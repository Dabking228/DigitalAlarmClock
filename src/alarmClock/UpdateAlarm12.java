	private void displayHours(){
		for(int i=1; i<=12; i++){
			String hour = String.format("%02d", i);
			setAlarmHour.addItem(hour);
		}
	}
	
	private void displayMinutes(){
		for(int i=0; i<=59; i++){
			String minute = String.format("%02d", i);
			setAlarmMinute.addItem(minute);
		}
	}
	
