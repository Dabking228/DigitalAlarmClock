    private void updateAlarmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAlarmBtnActionPerformed
        // TODO add your handling code here:


        Time12 time12 = new Time12();
        ArrayList<ArrayList<String>> alarms12 = time12.getAlarms12();
        boolean alarmExists = false;

        for (ArrayList<String> alarm12 : alarms12) {
            if(am_pm.equals(alarm12.get(2)) && hour.equals(alarm12.get(0)) && minute.equals(alarm12.get(1))){
                alarmExists = true;
                break;
            }
        }

        if(alarmExists){
            JOptionPane.showMessageDialog(this, "Alarm already exists!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            time12.addAlarm12(hour, minute, am_pm);
            Time24 time24 = new Time24();
            String hour24 = time12.convert12To24(hour, am_pm);
            time24.addAlarm24(hour24, minute);
            JOptionPane.showMessageDialog(this, "Alarm Set!");
        }
    }//GEN-LAST:event_updateAlarmBtnActionPerformed

